package main

import "fmt"

func main() {
	var num1 int = 20
	var num2 int = 10
	fmt.Println("num1 + num2 =",num1+num2) // 30
	fmt.Println("num1 - num2 =",num1-num2) // 10
	fmt.Println("num1 * num2 =",num1*num2) // 200
	fmt.Println("num1 / num2 =",num1/num2) // 2
	fmt.Println("num1 % num2 =",num1%num2) // 0
}